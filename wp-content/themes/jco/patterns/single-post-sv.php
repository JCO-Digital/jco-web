<p>Grand One on Suomen suurin digitaalisen markkinoinnin ja viestinnän kilpailu, jossa on jo yli 20 vuoden ajan palkittu Suomen parhaimmistoa digitaalisten palveluiden osaamisessa. Palveluita arvioidan eri kategorioissa&nbsp; kuten käyttökokemuksessa, palvelumuotoilussa ja designissa – me olimme bisnespelillämme ehdolla alle 60 tuhannen euron “Paras digitaalinen palvelu” -kategoriassa.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Grand One on alan merkittävin tapahtuma Suomessa. Olemme erittäin otettuja ja kiitollisia saamastamme tunnustuksesta. Mistä iloitsemme erityisesti on se, että pelillistäminen, johon uskomme vakaasti, on näyttänyt voimansa – ei ole aihetta, jota ei voisi pelillistää. Sydäntä lämmittää myös se, että asiakkaamme hanke menestyy ja saa näkyvyyttä.</p>
